public abstract class Enemy{

    protected static float hp;
    protected static float dmg;
    protected static int color;
    protected static

    private float getDamage(){
        return dmg;
    }

    private int getColor(){
        return color;
    }

    private boolean getHurtDie(float damage){
        hp-=damage;
        if(hp<=0) return true;
        else return false;
    }
}


